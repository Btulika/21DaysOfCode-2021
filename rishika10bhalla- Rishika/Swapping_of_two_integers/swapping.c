/*CODE TO SWAP TWO NUMBER*/ 
int main(){
    int a,b,c;
    printf("ENTER THE FIRST NUMBER: ",a);
    scanf("%i",&a);
    printf("ENTER THE SECOND NUMBER: ",b);
    scanf("%i",&b);
    c=a;                                
    a=b;
    b=c;

    printf("after the swapping first number is:%i \n",a);
    
    
    printf("after the swapping second number is: %i",b);

}
